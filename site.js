const metaDescriptionNode = document.getElementById("meta-description");

function applyLanguage(lang) {
  const dictionary = translations[lang] || translations.en;

  document.documentElement.lang = lang;
  document.title = dictionary.metaTitle;

  if (metaDescriptionNode) {
    metaDescriptionNode.setAttribute("content", dictionary.metaDescription);
  }

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    const key = node.getAttribute("data-i18n-html");
    if (dictionary[key]) {
      node.innerHTML = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.getAttribute("data-i18n-placeholder");
    if (dictionary[key]) {
      node.setAttribute("placeholder", dictionary[key]);
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });

  localStorage.setItem("aga-language", lang);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang);
  });
});

const preferredLanguage = localStorage.getItem("aga-language") || "en";
applyLanguage(preferredLanguage);

if (contactForm) {
  contactForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const currentLanguage = localStorage.getItem("aga-language") || "en";
    const dictionary = translations[currentLanguage] || translations.en;
    const formData = new FormData(contactForm);
    const name = formData.get("name") || (currentLanguage === "pl" ? "Klient" : "Client");
    const service = formData.get("service") || dictionary["form.serviceOption1"];

    const submitButton = contactForm.querySelector("button[type='submit']");
    if (submitButton) {
      submitButton.textContent = `${dictionary["form.savedPrefix"]} ${name} - ${service}, ${dictionary["form.savedSuffix"]}`;
      submitButton.disabled = true;
    }
  });
}
